import java.io.*;
public class TextFileRead 
{
    public static void main(String[] args) {
        try {
            FileReader reader = new FileReader("Assignment.txt");
            int character;
 
            while ((character = reader.read()) != -1) {
                System.out.print((char) character);
            }
            reader.close();
 
        } catch (IOException e) {
            e.printStackTrace();
        }
    } 
}
